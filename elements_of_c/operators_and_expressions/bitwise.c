#include<stdio.h>
int main()
{
    int a = 24;   // 00000000 00000000 00000000 00011000 - 0x18
    int b = 127;  // 00000000 00000000 00000000 01111111 - 0x7F
    int c;

    c = a&b;     // 00000000 00000000 00000000 00011000 - 0x18
    printf("c: %X\n", c);

    c = a|b;     // 00000000 00000000 00000000 01111111 - 0x7F
    printf("c: %X\n", c);

    c = ~a;     // 00000000 00000000 00000000 00011000 - 0x18 
                // 11111111 11111111 11111111 11100111 - 0xFFFFFFE7
    printf("c: %X\n", c);
    
    c= a<<1;    // 00000000 00000000 00000000 00011000 - 0x18
                // 00000000 00000000 00000000 00110000 - 0x30 

    printf("c:%X\n", c);

    c= a>>1;    // 00000000 00000000 00000000 00011000 - 0x18
                // 00000000 00000000 00000000 00001100 - 0x0C 
    printf("c:%X\n", c);
                 
                 // MSB                        LSB 
    c = a^b;     // 00000000 00000000 00000000 00011000 - 0x18
                 // 00000000 00000000 00000000 01111111 - 0x7F
                 // 00000000 00000000 00000000 01100111 - 0x67
                 //   0x1003   0x1002   0x1001   0x1000  # Little indian
                 
                 // LSB                        MSB
                 // 00011000 00000000 00000000 00000000
                 //   0x1003   0x1002   0x1001   0x1000  # BIG indian
    printf("c: %X\n", c);

    if((1&0xFF) == 1){
        printf("Processor is Little indian\n");
    }
    else {
        printf("Processor is big indian\n");
    }

    return 0;
}